/*
Sort an array alphabetically using the sort method.

Use the sort method in the alphabeticalOrder function to sort the items of arr in alphabetical order. The function should return the sorted array.
*/

// Solution:

function alphabeticalOrder(arr) {
    return arr.sort();
}

console.log(alphabeticalOrder(["a", "d", "c", "a", "z", "g"]));  // This will print ["a", "a", "c", "d", "g", "z"]


/*
    Explained:

    In this code, the sort() method will rearrange the elements of the arr array in alphabetical order.
*/